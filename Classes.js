class Alumnus {
    constructor(name, year, age, employment, seats, code) {
        this.name = name;
        this.year = year;
        this.age = age;
        this.employment = employment;
        this.seats = seats;
        this.code = code;
    }
}


class Participant {
    constructor(name, year, age, employment) {
        this.alumnusName = name;
        this.alumnusYear = year;
        this.alumnusAge = age;
        this.alumnusEmployment = employment;
    }
}
